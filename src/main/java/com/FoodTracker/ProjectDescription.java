package main.java.com.FoodTracker;

public class ProjectDescription {
	/**
	 * A description of the food tracking app.
	 * @author phoenix
	 *
	 */
	/*
	 * Food often goes to waste in our fridges, and this app is designed
	 * to help mitigate that occurrence. In addition to being a searchable log that keeps
	 * track of what is in the fridge, pantry, and freezer at any given time, this app
	 * will also provide reminders to the user when food is close to expiring. Another
	 * feature is to be able to check if a recipe has the ingredients available on hand,
	 * or if it should be added to a grocery list, which the app will also generate for
	 * the user at regular intervals, comprised of all the food that went bad or was used
	 * since the last generation, and can have items added manually. Users can also
	 * categorize the foods with tags they decide and search by category. This app will
	 * use persistent memory so as to be useful after closing the program. Any food can be
	 * removed or added, and any feature has the ability to add or remove foods. Favorite
	 * recipes can be stored and categorized as well. Lastly, a basic GUI will be implemented
	 * to make navigation easier, though it will also be command line usable as well.
	 *
	 *System requirements:
	 *
	 *10: Stores food by category as well as other info, such as expiration date
	 *10: Displays food available based on some order
	 *10: Can add/remove food to the list
	 *3: Recipe storage
	 *3: Favorite foods listing
	 *10: Persistent memory
	 *7: Grocery list generation
	 *9: Command menu
	 *7: Search-able lists
	 *5: Leftover storage/retrieval
	 *3: Food by flavor/type
	 *1: GUI
	 *
	 *User Stories:
	 *
	 *10: User can use this program to store food in a database.
	 *2: User can see stored food based on expiration date/other info.
	 *1: User's input is stored in categories.
	 *5: User's info is stored between sessions.
	 *3: User interacts with the program via command menu.
	 *3: User can search lists by food name/other.
	 *2: Program will generate shopping lists for user.
	 *2: User can store special food type, "leftovers."
	 *2: User can sort food based on flavor/type.
	 *2: User can store and retrieve favorite foods.
	 *2: User can store and retrieve recipes.
	 *10: User can interact via GUI.
	 */
}
